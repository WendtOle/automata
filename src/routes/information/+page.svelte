		<p>Setup</p>
		<ul>
			<li>remove 30 cards from deck (with Alpine 45)</li>
			<li>put one winter card in last ten cards and shuffle</li>
			<li>draw six cards to the hand</li>
			<li>fill clearing with five cards</li>
			<li>Select a challenge and a level</li>
		</ul>
	</div>
	<div class="section">
		<p>Gameplay</p>
		<ul>
			<li>after each turn an automata card is drawn and executed</li>
			<li>if you discard cards you always place them to the right of the clearing</li>
			<li>if there are wholes in the clearing the cards are pushed together</li>
		</ul>
	</div>
	<div class="section">
		<p>End of game</p>
		<ul>
			<li>check if the choosen challenge is met</li>
			<li>"Linden Tree" only give three points if you have at least 3 of them</li>
			<li>wookdpecker scores ten points if you have at least 10 trees</li>
		</ul>
